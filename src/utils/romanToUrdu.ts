// Improved phonetic map — only used for words NOT found in dictionary
// These are applied in sequence on the whole word
// Strategy: map common Urdu phoneme clusters first, then singles
const phoneticRules: [RegExp, string][] = [
  // Aspirated consonants (must come before base letter)
  [/bh/g, "بھ"],
  [/ph/g, "پھ"],
  [/th/g, "تھ"],
  [/dh/g, "دھ"],
  [/jh/g, "جھ"],
  [/kh/g, "کھ"],
  [/gh/g, "گھ"],
  [/ch/g, "چھ"],
  [/rh/g, "ڑھ"],
  [/nh/g, "نھ"],
  [/lh/g, "لھ"],

  // Digraphs
  [/sh/g, "ش"],
  [/zh/g, "ژ"],
  [/ng/g, "نگ"],

  // Long vowels (must come before short)
  [/aa/g, "آ"],
  [/ee/g, "ی"],
  [/ii/g, "ی"],
  [/oo/g, "و"],
  [/uu/g, "و"],
  [/ai/g, "ے"],
  [/ay/g, "ے"],
  [/au/g, "او"],
  [/aw/g, "او"],

  // Consonants
  [/b/g, "ب"],
  [/c/g, "ک"],
  [/d/g, "د"],
  [/f/g, "ف"],
  [/g/g, "گ"],
  [/h/g, "ہ"],
  [/j/g, "ج"],
  [/k/g, "ک"],
  [/l/g, "ل"],
  [/m/g, "م"],
  [/n/g, "ن"],
  [/p/g, "پ"],
  [/q/g, "ق"],
  [/r/g, "ر"],
  [/s/g, "س"],
  [/t/g, "ت"],
  [/v/g, "و"],
  [/w/g, "و"],
  [/x/g, "کس"],
  [/y/g, "ی"],
  [/z/g, "ز"],

  // Short vowels (after consonants)
  [/a/g, "ا"],
  [/e/g, "ے"],
  [/i/g, "ی"],
  [/o/g, "و"],
  [/u/g, "و"],
];

function transliterateUnknownWord(word: string): string {
  const lower = word.toLowerCase();
  let result = lower;
  for (const [pattern, replacement] of phoneticRules) {
    result = result.replace(pattern, replacement);
  }
  return result;
}

export function romanToUrdu(text: string): string {
  if (!text.trim()) return "";

  // Sort dictionary keys by length descending — match longer phrases first
  const sortedKeys = Object.keys(dictionary).sort((a, b) => b.length - a.length);

  // Use a placeholder approach: replace matched dictionary entries with tokens
  // then do phonetic fallback on remaining latin text
  const tokens: string[] = [];
  let processed = text;

  // Replace dictionary matches (whole word / phrase boundary)
  for (const key of sortedKeys) {
    // Escape special regex chars in key
    const escapedKey = key.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const regex = new RegExp(`(?<![\\u0600-\\u06FF\\w])${escapedKey}(?![\\u0600-\\u06FF\\w])`, "gi");
    processed = processed.replace(regex, (match) => {
      const token = `\x00${tokens.length}\x00`;
      tokens.push(dictionary[key]);
      return token;
    });
  }

  // Phonetic fallback for remaining latin words
  processed = processed.replace(/[a-zA-Z]+/g, (match) => {
    return transliterateUnknownWord(match);
  });

  // Restore dictionary tokens
  processed = processed.replace(/\x00(\d+)\x00/g, (_, idx) => tokens[parseInt(idx)]);

  // Convert punctuation to Urdu equivalents
  processed = processed
    .replace(/\?/g, "؟")
    .replace(/,/g, "،")
    .replace(/\./g, "۔")
    .replace(/;/g, "؛");

  return processed;
}

export function isRomanUrdu(text: string): boolean {
  const latinChars = (text.match(/[a-zA-Z]/g) || []).length;
  const totalChars = text.replace(/\s/g, "").length;
  return totalChars > 0 && latinChars / totalChars > 0.5;
}

