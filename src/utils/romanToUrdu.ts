// Get alternative transliterations for a word
export function getAlternatives(word: string): string[] {
  const lower = word.toLowerCase();
  if (alternativeTransliterations[lower]) {
    return alternativeTransliterations[lower];
  }
  // Return primary transliteration as first option, then alternatives
  const primary = romanToUrdu(word);
  return [primary];
}
